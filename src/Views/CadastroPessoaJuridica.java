    public void cadastrarPessoaJuridica(){
        pj.setNome(txtNome.getText());
        pj.setCnpj(Integer.parseInt(txtCNPJ.getText()));
        pj.setEndereco(txtEnd.getText());
        pj.setBairro(txtBairro.getText());
        pj.setCep(txtCEP.getText());
        pj.setCidade(txtCidade.getText());
        pj.setEstado(txtEstado.getText());
        pj.setEmail(txtEmail.getText());
        pj.setDdd(Integer.parseInt(txtDDD.getText()));
        pj.setTelefone(Integer.parseInt(txtTelefone.getText()));
        pj.setTipo(txtTipo.getText());
        
        
    }

